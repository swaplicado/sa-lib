    @Override
    public void resetPicker() {
        int[] period = SLibTimeUtils.digestMonth(miClient.getSession().getWorkingDate());

        mnPickerResult = SLibConsts.UNDEFINED;
        mbFirstActivation = true;

        moCalYear.setValue(period[0]);
        moCalMonth.setValue(period[1]);
    }

    @Override
    public void setPickerVisible(final boolean visible) {
        setVisible(visible);
    }

    @Override
    public void setOption(final Object option) {
        moCalYear.setValue(((int[]) option)[0]);
        moCalMonth.setValue(((int[]) option)[1]);
    }

    @Override
    public int[] getOption() {
        return new int[] { moCalYear.getValue(), moCalMonth.getValue() };
    }

    @Override
    public SGuiValidation validatePicker() {
        return moFields.validateFields();
    }

