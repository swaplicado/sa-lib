    /**
     * @param value Date type event, constants defined in SGridConsts (DATE_TYPE_...).
     */
    @Override
    public void initFilter(Object value) {
        switch ((Integer) value) {
            case SGridConsts.DATE_TYPE_EVT:
                jtbDateEvent.setSelected(true);
                moPaneView.getFiltersMap().put(SGridConsts.FILTER_DATE_TYPE, SGridConsts.DATE_TYPE_EVT);
                break;
            case SGridConsts.DATE_TYPE_DOC:
                jtbDateDocument.setSelected(true);
                moPaneView.getFiltersMap().put(SGridConsts.FILTER_DATE_TYPE, SGridConsts.DATE_TYPE_DOC);
                break;
            default:
                miClient.showMsgBoxError(SLibConsts.ERR_MSG_OPTION_UNKNOWN);
        }
    }
